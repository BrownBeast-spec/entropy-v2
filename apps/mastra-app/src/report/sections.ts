/**
 * Extracts citation-like references (DOIs, PMIDs, URLs) from text.
 * Returns an array of { key, ref } objects for the bibliography.
 */
export function extractCitations(
  text: string,
): Array<{ key: string; ref: string }> {
  const citations: Array<{ key: string; ref: string }> = [];

  // Match DOIs: 10.XXXX/...
  const doiRegex = /\b(10\.\d{4,}\/[^\s,;)\]]+)/g;
  let m: RegExpExecArray | null;
  while ((m = doiRegex.exec(text)) !== null) {
    const doi = m[1];
    const key = "doi:" + doi.replace(/[^a-zA-Z0-9]/g, "_");
    if (!citations.find((c) => c.key === key)) {
      citations.push({ key, ref: `DOI: \\url{${doi}}` });
    }
  }

  // Match PMIDs: PMID: XXXXXXXX
  const pmidRegex = /PMID[:\s]+(\d{6,9})/gi;
  while ((m = pmidRegex.exec(text)) !== null) {
    const pmid = m[1];
    const key = "pmid:" + pmid;
    if (!citations.find((c) => c.key === key)) {
      citations.push({
        key,
        ref: `PubMed ID: ${pmid}. \\url{https://pubmed.ncbi.nlm.nih.gov/${pmid}/}`,
      });
    }
  }

  // Match URLs (http/https)
  const urlRegex = /https?:\/\/[^\s,;)\]>\"]+/g;
  while ((m = urlRegex.exec(text)) !== null) {
    const url = m[0].replace(/[.,;)\]]+$/, ""); // strip trailing punctuation
    const key = "url:" + url.replace(/[^a-zA-Z0-9]/g, "_").slice(0, 40);
    if (!citations.find((c) => c.key === key)) {
      citations.push({ key, ref: `\\url{${url}}` });
    }
  }

  return citations;
}

/** Collect all citations from the report input */
export function collectAllCitations(
  input: ReportInput,
): Array<{ key: string; ref: string }> {
  const allText = [
    input.evidence.agents.biologist.content,
    input.evidence.agents.clinicalScout.content,
    input.evidence.agents.hawk.content,
    input.evidence.agents.librarian.content,
    input.verificationReport.summary,
    input.gapAnalysis.summary,
  ].join("\n");

  return extractCitations(allText);
}

/** Insert \cite{key} commands for DOIs/PMIDs in content */
function inlineCitations(
  text: string,
  citations: Array<{ key: string; ref: string }>,
): string {
  let result = text;
  for (const { key, ref } of citations) {
    // Try to match the original identifier back from ref
    const doiMatch = ref.match(/DOI: \\url\{([^}]+)\}/);
    const pmidMatch = ref.match(/PubMed ID: (\d+)/);
    const urlMatch = ref.match(/\\url\{([^}]+)\}/);

    if (doiMatch) {
      result = result.replace(
        new RegExp(escapeRegex(doiMatch[1]), "g"),
        `$& \\cite{${key}}`,
      );
    } else if (pmidMatch) {
      result = result.replace(
        new RegExp(`PMID[:\\s]+${pmidMatch[1]}`, "gi"),
        `$& \\cite{${key}}`,
      );
    } else if (urlMatch) {
      result = result.replace(
        new RegExp(escapeRegex(urlMatch[1]), "g"),
        `$& \\cite{${key}}`,
      );
    }
  }
  return result;
}

function escapeRegex(s: string): string {
  return s.replace(/[.*+?^${}()|[\]\\]/g, "\\$&");
}

// ─── Section Generators ───────────────────────────────────────────────────────

export function titlePageSection(input: ReportInput): string {
  const title = escapeLatex(input.query);
  const timestamp = escapeLatex(input.metadata.timestamp);
  const sessionId = escapeLatex(input.metadata.sessionId ?? "N/A");
  const reviewer = escapeLatex(input.reviewerDecision.reviewer);

  return `\\begin{titlepage}
\\centering
{\\Huge\\bfseries Drug Repurposing Dossier\\par}
\\vspace{1cm}
{\\Large\\bfseries ${title}\\par}
\\vspace{2cm}
{\\large
  \\textbf{Timestamp:} ${timestamp}\\\\[0.5em]
  \\textbf{Session ID:} ${sessionId}\\\\[0.5em]
  \\textbf{Reviewer:} ${reviewer}\\\\[0.5em]
}
\\vfill
{\\small Generated by Entropy Research Pipeline}
\\end{titlepage}`;
}

