export function gapAnalysisSection(input: ReportInput): string {
  const ga = input.gapAnalysis;

  // TPP checklist table
  const rows = ga.tppChecklist
    .map(
      (item) =>
        `  ${escapeLatex(item.category)} & ${escapeLatex(item.status)} & ${escapeLatex(item.notes)} \\\\`,
    )
    .join("\n");

  const tppTable =
    ga.tppChecklist.length > 0
      ? `\\subsection{TPP Checklist}
\\begin{tabular}{|l|c|p{8cm}|}
\\hline
\\textbf{Category} & \\textbf{Status} & \\textbf{Notes} \\\\
\\hline
${rows}
\\hline
\\end{tabular}`
      : `\\subsection{TPP Checklist}\nNo checklist items.`;

  // Present evidence
  const presentEvidence =
    ga.presentEvidence.length > 0
      ? ga.presentEvidence
          .map(
            (e) =>
              `  \\item \\textbf{${escapeLatex(e.category)}} (${escapeLatex(e.strength)}): ${escapeLatex(e.description)}`,
          )
          .join("\n")
      : "  \\item None";

  // Missing evidence
  const missingEvidence =
    ga.missingEvidence.length > 0
      ? ga.missingEvidence
          .map(
            (g) =>
              `  \\item \\textbf{${escapeLatex(g.category)}} [${escapeLatex(g.severity)}]: ${escapeLatex(g.description)} --- \\textit{${escapeLatex(g.recommendation)}}`,
          )
          .join("\n")
      : "  \\item None";

  // Contradictions
  const contradictions =
    ga.contradictions.length > 0
      ? ga.contradictions
          .map(
            (c) =>
              `  \\item [${escapeLatex(c.severity)}] ${escapeLatex(c.description)} (${escapeLatex(c.sourceA.agentId)} vs ${escapeLatex(c.sourceB.agentId)})`,
          )
          .join("\n")
      : "  \\item None";

  // Risk flags
  const riskFlags =
    ga.riskFlags.length > 0
      ? ga.riskFlags
          .map(
            (r) =>
              `  \\item \\textbf{${escapeLatex(r.flag)}} [${escapeLatex(r.severity)}]: ${escapeLatex(r.description)}`,
          )
          .join("\n")
      : "  \\item None";

  return `\\section{Gap Analysis}

${tppTable}

\\subsection{Present Evidence}
\\begin{itemize}
${presentEvidence}
\\end{itemize}

\\subsection{Missing Evidence}
\\begin{itemize}
${missingEvidence}
\\end{itemize}

\\subsection{Contradictions}
\\begin{itemize}
${contradictions}
\\end{itemize}

\\subsection{Risk Flags}
\\begin{itemize}
${riskFlags}
\\end{itemize}

\\subsection{Overall Readiness}
${escapeLatex(ga.overallReadiness)}`;
}

export function verificationReportSection(input: ReportInput): string {
  const vr = input.verificationReport;

  const claimRows = vr.claims
    .map(
      (c) =>
        `  ${escapeLatex(c.claimId)} & ${escapeLatex(c.verificationStatus)} & ${(c.confidence * 100).toFixed(0)}\\% & ${escapeLatex(c.discrepancy ?? "---")} \\\\`,
    )
    .join("\n");

  const claimsTable =
    vr.claims.length > 0
      ? `\\begin{tabular}{|l|c|c|p{6cm}|}
\\hline
\\textbf{Claim ID} & \\textbf{Status} & \\textbf{Confidence} & \\textbf{Discrepancy} \\\\
\\hline
${claimRows}
\\hline
\\end{tabular}`
      : "No claims checked.";

  return `\\section{Verification Report}

\\textbf{Overall Integrity:} ${escapeLatex(vr.overallIntegrity)}\\\\
\\textbf{Total Claims Checked:} ${vr.totalClaimsChecked}\\\\
\\textbf{Confirmed:} ${vr.confirmedCount}\\\\
\\textbf{Flagged:} ${vr.flaggedCount}\\\\
\\textbf{Unverifiable:} ${vr.unverifiableCount}

\\subsection{Claims Table}
${claimsTable}`;
}

